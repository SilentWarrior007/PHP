        <h3>Virtual Super Resolution</h3>
        <p>Super Sampling Anti-Aliasing (SSAA) is one of many methods of anti-aliasing to enable smoother textures and less 
        jaggy polygons in games, but is not supported by all games and engines. Virtual Super Resolution (VSR) is an AMD feature 
        that is game and engine agnostic to simulate SSAA in games that do not have native SSAA support. VSR can also be used in 
        conjunction with other native in-game anti-aliasing for even more game graphics-setting control. VSR allows games to 
        render at higher resolutions (up to 4K; see the ‘Virtual Super Resolution Support’ table, below) and then rescales them 
        down to a lower native display resolution. Using this, you can get quality that rivals up to 4K, even on a 1080p display 
        while playing your favorite games. One of the coolest aspects of this technology is that the performance is no different 
        than if you were running on a normal 4K monitor. Virtually nothing is lost in the scaling of the image. You just end up 
        with amazing visual quality on a 1080p monitor, with the same FPS you would achieve with a regular 4K monitor. See below 
