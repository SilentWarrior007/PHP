<h1 class="myhead3">Advanced Micro Devices</h1>
<p>Advanced Micro Devices, Inc. (AMD) is an American multinational semiconductor company based in Santa Clara, California and Austin, 
    Texas that develops computer processors and related technologies for business and consumer markets. While initially it 
    manufactured its own processors, the company later outsourced its manufacturing, a practice known as fabless, after 
    GlobalFoundries was spun off in 2009. AMD's main products include microprocessors, motherboard chipsets, embedded 
    processors and graphics processors for servers, workstations and personal computers, and embedded systems applications.
    Founded in 1969 as a Silicon Valley start-up, the AMD journey began with dozens of employees focused on leading-edge 
    semiconductor products. From those modest beginnings, AMD has grown into a global company of 10,000 people, achieving 
    many important industry firsts along the way. AMD today develops high-performance computing and visualization products to 
    solve some of the world’s toughest and most interesting challenges. There has never been a better time to be in the 
    semiconductor industry, and we are ready to tackle the next 50 years with high-performance computing and graphics solutions 
    that transform all of our lives.</p>
    <h3 class="myhead3">History</h3>   
<p>Advanced Micro Devices was formally incorporated on May 1, 1969, by Jerry Sanders, along with seven of his colleagues from 
Fairchild Semiconductor. Sanders, an electrical engineer who was the director of marketing at Fairchild, had, like many 
Fairchild executives, grown frustrated with the increasing lack of support, opportunity, and flexibility within the company, and 
decided to leave to start his own semiconductor company. The previous year Robert Noyce, who had developed the first silicon 
integrated circuit in 1959 at Fairchild, had left Fairchild together with Gordon Moore and founded the semiconductor company 
Intel in July 1968. In September 1969, AMD moved from its temporary location in Santa Clara to Sunnyvale, California. To 
immediately secure a customer base, AMD initially became a second source supplier of microchips designed by Fairchild and National 
Semiconductor. AMD first focused on producing logic chips. The company guaranteed quality control to United States 
Military Standard, an advantage in the early computer industry since unreliability in microchips was a distinct problem that 
customers – including computer manufacturers, the telecommunications industry, and instrument manufacturers – wanted to avoid.</p>
